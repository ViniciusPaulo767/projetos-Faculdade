package Aulas;

public class Resolucao_dos_Exercicios2_tabuadaFor {

    public static void main(String[] args) {
        int res,num1;
        num1=5;
        
        for(int i=0;i<=10;i++){
            res=num1*i;
            System.out.println("O resultado de " + num1 +"x" + i + "=" + res);
        }
    } 
}
